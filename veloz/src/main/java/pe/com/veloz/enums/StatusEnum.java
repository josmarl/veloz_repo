package pe.com.veloz.enums;

/**
 *
 * @author josmarl
 */
public enum StatusEnum {

    OK(1l, "Success"),
    ERROR(2l, "Failed");

    private Long id;
    private String status;

    private StatusEnum(Long id, String status) {
        this.id = id;
        this.status = status;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
